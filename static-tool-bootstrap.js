/* static-tool-bootstrap.js — mounts the REAL tool on its static SEO page.
 *
 * Load order (all deferred, after the inline fav/share script):
 *   config.js → utils.js → tool-helpers.js → data.js → {category}-tools.js → this
 *
 * Feasibility verified in code: INIT{} is declared in data.js (not app.js),
 * and every INIT[slug](panel) across all five category files is fully
 * self-contained — zero calls to go()/openTool()/_ga(). Panel component CSS
 * (.drop/.status/.results/.btn) comes from tools.css, linked in the head,
 * so the tool renders pixel-identical to its in-app version.
 *
 * Fav + share are handled by the page's inline script (generated by
 * build-tool-pages.js) — this file deliberately does NOT touch them.
 */
(function () {
  'use strict';

  var SLUG = document.body.getAttribute('data-tool-slug');
  if (!SLUG) return;

  /* Safety stub — if config.js somehow failed to load before this file
     runs, _ga must still be callable without throwing. Real analytics
     wiring lives in analytics.js (trackEvent) and config.js (_ga/gtag),
     both loaded earlier in the page's script order; this is just a
     defensive fallback, not the primary path. */
  window._ga = window._ga || function () {};

  function ready(fn) {
    if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', fn);
    else fn();
  }

  ready(function () {
    /* ── Mount the tool ──────────────────────────────────────────── */
    var panel = document.getElementById('toolPanel');
    if (panel) {
      var mounted = false;
      try {
        if (typeof INIT !== 'undefined' && typeof INIT[SLUG] === 'function') {
          panel.innerHTML = '';
          INIT[SLUG](panel);
          mounted = true;
          if (window.trackEvent) window.trackEvent('tool_open', {});
          /* Universal Reset (shared system in tool-helpers.js): one
             button, every tool, no page refresh needed. Injected after
             the panel so a reset (which wipes the panel) never removes
             its own control. */
          if (window.TarumakReset && !document.getElementById('toolResetBar')) {
            var bar = document.createElement('div');
            bar.id = 'toolResetBar';
            bar.style.cssText = 'text-align:center;margin-top:10px';
            var rb = document.createElement('button');
            rb.className = 'btn btn-ghost';
            rb.style.cssText = 'height:34px;padding:0 16px;font-size:13px';
            rb.innerHTML = '&#8634;\u00a0 Start over';
            rb.setAttribute('aria-label', 'Reset this tool to its initial state');
            rb.onclick = function () { window.TarumakReset(); };
            bar.appendChild(rb);
            panel.insertAdjacentElement('afterend', bar);
          }
        }
      } catch (e) {
        if (window.console) console.warn('[tool-page] mount failed:', e);
      }
      if (!mounted) {
        var name = document.body.getAttribute('data-tool-name') || 'the tool';
        panel.innerHTML =
          '<div style="text-align:center;padding:36px 16px">' +
            '<p style="color:var(--text-dim);font-size:14.5px;margin-bottom:16px">This tool could not load here \u2014 it may be temporarily unavailable.</p>' +
            '<a href="/tools" style="display:inline-flex;padding:11px 22px;border-radius:11px;background:var(--p1);color:#0b0f16;font-weight:600;font-size:14px;text-decoration:none">Browse all tools</a>' +
          '</div>';
      }
    }

    /* ── Recents — same {slug,ts} shape features.js normalizes ────── */
    try {
      var RK = 'tmk_recent';
      var arr = JSON.parse(localStorage.getItem(RK) || '[]');
      if (Array.isArray(arr)) {
        arr = arr.filter(function (e) { return e && e.slug !== SLUG; });
        arr.unshift({ slug: SLUG, ts: Date.now() });
        localStorage.setItem(RK, JSON.stringify(arr.slice(0, 12)));
      }
    } catch (e) { /* private mode — non-essential */ }
  });
})();
